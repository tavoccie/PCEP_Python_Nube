# podemos imprimir multiples lineas con un unico print funcion 
# usando backslash n o \n
print("Hola Mundo\nHola Mundo\nHola Mundo")

# concatenar string usando el operador +
# como NO hay espacio entre las comillas las dos palabras se pegan, unen.
print("ultra" + "tavo")

# Dar espacio a las palabras concatenada, formas:
print("ultra " + "tavo")
print("ultra" + " tavo")
print("ultra" +" "+ "tavo")

